# You need 3 dragon scales (10 gold each) and 5 elf tears (3 gold each). Calculate the total cost.
one_gold_sclae = 10
one_elf_tear = 3
gold_scales_needed = 3* one_gold_sclae
elf_tears_needed = 5* one_elf_tear
